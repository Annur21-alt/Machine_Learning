#Write a Python program that:
#Prompts the user to enter a number. 
#Calculates the square and cube of the number. 
#Prints the square and cube.

print("This program will show the result of the square and cube of the number.")

num = input("Please enter the number:")

square_of_number = int(num) ** 2
cube_of_number = int(num) ** 3 

print("Square of the number:", square_of_number)
print("Cube of the number:", cube_of_number)
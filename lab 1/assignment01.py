#Performs and prints the results of addition, subtraction, multiplication, 
#and division of these two numbers.

print("This program will show the total of addition, subtraction, multiplication and division of two integer number.")

firstnum = input("Please enter first integer number:")
secondnum = input("Please enter second integer number:")


print("Total addition:", int(firstnum) + int (secondnum))
print("Total subtraction:", int(firstnum) - int (secondnum))
print("Total multiplication:", int(firstnum) * int (secondnum))
print("Total division:", int(firstnum) / int(secondnum))
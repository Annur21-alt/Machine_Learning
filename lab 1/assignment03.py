#Prompts the user to enter the length and width of a rectangle. 
#Calculates the area and perimeter of the rectangle. 
#Prints the area and perimeter.

print("This program will show the result of area and perimeter of a rectangle.")

length = input("Please enter the length of a rectangle:")
width = input("Please enter the width of a rectangle:")

print("Area of Rectangle:", int(length) * int(width))
print("Perimeter of Rectangle:", int(length) + int(length) + int(width) + int(width))




